package t12.util;

public class IntToChar {
	public char intToChar (int i) {
		char c = 0;
		switch (i) {
		case 2:
			c = '2';
			break;
		case 3:
			c = '3';
			break;
		case 4:
			c = '4';
			break;	
		case 5:
			c = '5';
			break;
		case 6:
			c = '6';
			break;
		case 7:
			c = '7';
			break;
		case 8:
			c = '8';
			break;
		case 9:
			c = '9';
			break;
		default:
			break;
		}
		return c;
	}
}
